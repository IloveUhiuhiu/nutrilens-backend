def lookup_first_usda_foods_for_history(stdout=None, style=None):
    """Tra USDA cho từng query mẫu và lưu Food đầu tiên trả về, bỏ qua query không có kết quả."""
    foods = {}
    skipped_queries = []
    for dish in USDA_TEXT_DISHES:
        try:
            payload = search_usda_foods(dish["usda_query"], page_size=1, page_number=1)
        except ExternalLookupError as exc:
            skipped_queries.append(dish["vi_query"])
            if stdout and style:
                stdout.write(style.WARNING(f"Skipped USDA dish {dish['vi_query']}: {exc.public_message}"))
            continue

        food_payloads = payload.get("foods") or []
        if not food_payloads:
            skipped_queries.append(dish["vi_query"])
            if stdout and style:
                stdout.write(style.WARNING(f"Skipped USDA dish {dish['vi_query']}: no USDA result."))
            continue

        food_payload = food_payloads[0]
        food = save_food_from_usda_payload(food_payload)
        food.vi_name = dish["vi_query"]
        food.en_name = food_payload.get("description") or food.en_name
        food.save(update_fields=["vi_name", "en_name"])
        foods[dish["vi_query"]] = {
            "food": food,
            "payload": food_payload,
            "vi_query": dish["vi_query"],
            "usda_query": dish["usda_query"],
        }
    return foods, skipped_queries


def build_seed_datetime(day, hour, minute):
    """Tạo datetime aware theo timezone hiện tại cho meal_time/confirmed_at."""
    naive_value = datetime.combine(day, time(hour=hour, minute=minute))
    return timezone.make_aware(naive_value, timezone.get_current_timezone())


def update_seed_meal_timestamp(meal, timestamp):
    """Cập nhật meal_time vì field auto_now_add không nhận giá trị lúc create."""
    MealEntry.objects.filter(id=meal.id).update(meal_time=timestamp, confirmed_at=timestamp)
    meal.meal_time = timestamp
    meal.confirmed_at = timestamp
    return meal


