                admin.full_name = admin.full_name or f"Admin {index}"
                admin.activity_level = admin.activity_level or default_activity_level
                admin.is_active = True
                admin.is_staff = True
                admin.is_superuser = True
                if not admin.avatar_url:
                    admin.avatar_url = get_random_default_avatar_url()
                admin.save(
                    update_fields=[
                        "password",
                        "full_name",
                        "activity_level",
                        "is_active",
                        "is_staff",
                        "is_superuser",
                        "avatar_url",
                    ]
                )

